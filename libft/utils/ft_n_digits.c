#include "utils.h"

unsigned int	ft_n_digits(int n)
{
	unsigned int	d;

	d = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		d++;
	while (n != 0)
	{
		n /= 10;
		d++;
	}
	return (d);
}
