#include "utils.h"

unsigned int	ft_n_d_uns(unsigned int n)
{
	unsigned int	d;

	d = 0;
	if (n == 0)
		return (1);
	while (n > 0)
	{
		n /= 10;
		d++;
	}
	return (d);
}
