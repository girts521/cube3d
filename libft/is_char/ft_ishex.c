#include "is_char.h"

int	ft_ishex(int c)
{
	if (c >= 'A' && c <= 'F')
		c += 32;
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'f'))
		return (1);
	return (0);
}
